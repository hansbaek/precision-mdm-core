-- 승인 워크플로 + 인앱 알림 기반 스키마 (Phase 1).
-- TypeORM synchronize=false 이므로 운영 DB에 수동 적용해야 한다.

-- 1) RBAC: 승인(approve) 액션 플래그 추가.
ALTER TABLE TMDM_ROLE_MENU_PERM ADD (CAN_APPROVE_YN VARCHAR2(1) DEFAULT 'N' NOT NULL);
COMMENT ON COLUMN TMDM_ROLE_MENU_PERM.CAN_APPROVE_YN IS '변경요청 직접 반영 + 승인/반려 권한 (Y/N)';

-- 2) 인앱 알림.
CREATE TABLE TMDM_NOTIFICATION (
  NOTI_ID       NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  RECIPIENT_ID  VARCHAR2(50)  NOT NULL,
  NOTI_TYPE     VARCHAR2(40)  NOT NULL,
  MESSAGE       VARCHAR2(500) NOT NULL,
  LINK          VARCHAR2(200),
  IS_READ_YN    VARCHAR2(1)   DEFAULT 'N' NOT NULL,
  CREATED_AT    TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL
);
-- 수신자별 안읽음 조회 가속.
CREATE INDEX IX_TMDM_NOTI_RECIPIENT ON TMDM_NOTIFICATION (RECIPIENT_ID, IS_READ_YN);

-- 3) 변경요청(승인 워크플로).
CREATE TABLE TMDM_CHANGE_REQUEST (
  CR_ID          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  TARGET_TYPE    VARCHAR2(40)  NOT NULL,
  OPERATION      VARCHAR2(10)  NOT NULL,
  TARGET_ID      NUMBER,
  PAYLOAD        VARCHAR2(4000),
  SUMMARY        VARCHAR2(500),
  REQUESTER_ID   VARCHAR2(50)  NOT NULL,
  STATUS         VARCHAR2(10)  DEFAULT 'PENDING' NOT NULL,
  REVIEWER_ID    VARCHAR2(50),
  REVIEW_COMMENT VARCHAR2(500),
  CREATED_AT     TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL,
  REVIEWED_AT    TIMESTAMP
);
-- 대기 큐 조회 가속.
CREATE INDEX IX_TMDM_CR_STATUS ON TMDM_CHANGE_REQUEST (STATUS, CREATED_AT);
